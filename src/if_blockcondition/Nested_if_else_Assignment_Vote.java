package if_blockcondition;

public class Nested_if_else_Assignment_Vote 
{

	public static void main(String[] args) 
	{
		
		int age=20;
		char gender='M';
		char gender1='F';
			
		if(gender=='M')
		{	
			if(age>19);
		System.out.println("A male person is eligible to vote");	
		}
		else
		{
		System.out.println("A male person is not eligible to vote");	
		}
		
		if(gender1=='F')
		{	
			if(age<18);
		System.out.println("A female person is not eligible to vote");		
		}
		else
		{
			System.out.println("A female person is eligible to vote");	
		}
		
}
}
		
		